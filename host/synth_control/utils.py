class Colors:
    """ANSI color codes for console output. Add more utilities here as needed."""
    HEADER = '\033[95m'     # Magenta
    BLUE = '\033[94m'       # Blue
    CYAN = '\033[96m'       # Cyan
    GREEN = '\033[92m'      # Green
    YELLOW = '\033[93m'     # Yellow
    RED = '\033[91m'        # Red
    ORANGE = '\033[38;5;208m'  # Orange
    BOLD = '\033[1m'        # Bold
    UNDERLINE = '\033[4m'   # Underline
    END = '\033[0m'         # Reset to default
